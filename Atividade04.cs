using System;

internal class Principal
{
    public static void Main()
    {
        int n1 = 15, n2 = 2, soma, sub, mult, div, rd;

        soma = n1 + n2;
        sub = n1 - n2;
        mult = n1 * n2;
        div = n1 / n2;
        rd = n1 % n2;

        Console.WriteLine("Resultado Soma: " + soma);
        Console.WriteLine("Resultado Subtração: " + sub);
        Console.WriteLine("Resultado Multiplicação: " + mult);
        Console.WriteLine("Resultado Divisão: " + div);
        Console.WriteLine("Resultado Resto da Divisão: " + rd);
    }   
}




